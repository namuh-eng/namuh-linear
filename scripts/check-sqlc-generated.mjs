#!/usr/bin/env node
import fs from "node:fs";

const requiredFiles = [
  "apps/api/internal/sqlc/queries/issues.sql",
  "apps/api/internal/sqlc/generated/db.go",
  "apps/api/internal/sqlc/generated/issues.sql.go",
  "apps/api/internal/sqlc/generated/models.go",
];
const missingFiles = requiredFiles.filter((file) => !fs.existsSync(file));
if (missingFiles.length > 0) {
  console.error("sqlc issue query generation is missing files:");
  for (const file of missingFiles) console.error(`- ${file}`);
  console.error(
    'Run: docker run --rm -v "$PWD:/src" -w /src/apps/api sqlc/sqlc:1.28.0 generate',
  );
  process.exit(1);
}

const generated = fs.readFileSync(
  "apps/api/internal/sqlc/generated/issues.sql.go",
  "utf8",
);
const requiredSnippets = [
  "Code generated by sqlc. DO NOT EDIT.",
  "func (q *Queries) GetIssueByID",
  "func (q *Queries) GetIssueByIdentifier",
];
const missingSnippets = requiredSnippets.filter(
  (snippet) => !generated.includes(snippet),
);
if (missingSnippets.length > 0) {
  console.error("sqlc issue query generation is stale:");
  for (const snippet of missingSnippets) console.error(`- missing: ${snippet}`);
  process.exit(1);
}
console.log("sqlc generated issue queries present.");
